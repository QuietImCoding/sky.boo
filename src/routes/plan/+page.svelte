<h2>the boosky plan</h2>

<p>
    What is boosky? Is it an idea? A vibe?
    Where is the boosky stored? 

    <br><br>

    As it stands, the BlueSky team have created a great protocol for the social web, but the primary use case is Bluesky itself.
    Most people will sign up, give Bluesky their data, and think pretty little of it, like making a Twitter(RIP) account.

    <br><br>

    We think the ATproto can be so much more. Every day creative programmers are taking a look at the data-ownership systems built into Bluesky and finding fun new ways to create social experiences.
    So the hope is to separate the ATproto possibilities from the social network, and to give people a place to find and share resources, to customize profiles, and to cultivate an online presence that is truly their own.

    <br><br>
    To cover up some of the messy stuff and let artists and creative people of all sorts come together and share their passions on a new open web. 

    <br><br>

    We'll see you on boosky.

</p>

